package com.englishenglishdictionary.model;

public class WordnikDictionaryWordDefinition {
    String text;
    String partOfSpeech;

    @Override
    public String toString() {
        return "WordnikDictionaryWordDefinition{" +
                "text='" + text + '\'' +
                ", partOfSpeech='" + partOfSpeech + '\'' +
                '}';
    }
}
